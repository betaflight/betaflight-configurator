#!/usr/bin/env node
// Sync target silkscreen-default mappings from the
// `betaflight/unified-targets` repo into the configurator's bundled
// JSON used by the smart-dropdown's tier-0 padDefaults lookup.
//
// Usage:
//   node scripts/sync-unified-targets.mjs
//
// Reads:  https://github.com/betaflight/unified-targets/configs/default/*.config
// Writes: src/data/target-defaults.json
//
// The output is committed to the configurator repo so end users don't
// need network access at install time. Re-run before each release (or
// after a notable upstream silkscreen-revision change) and review the
// JSON diff in the PR.

import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import { parseUnifiedTargetConfig } from "./lib/parseUnifiedTargetConfig.mjs";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(SCRIPT_DIR, "..");
const CACHE_DIR = resolve(REPO_ROOT, ".cache/unified-targets");
const CONFIGS_DIR = resolve(CACHE_DIR, "configs/default");
const OUTPUT_FILE = resolve(REPO_ROOT, "src/data/target-defaults.json");
const REPO_URL = "https://github.com/betaflight/unified-targets.git";

function run(cmd, args, opts = {}) {
    return execFileSync(cmd, args, { stdio: "inherit", ...opts });
}

function ensureClone() {
    if (existsSync(join(CACHE_DIR, ".git"))) {
        console.log(`[sync] pulling latest unified-targets into ${CACHE_DIR}`);
        run("git", ["-C", CACHE_DIR, "fetch", "--depth=1", "origin", "master"]);
        run("git", ["-C", CACHE_DIR, "reset", "--hard", "origin/master"]);
        return;
    }
    console.log(`[sync] cloning unified-targets to ${CACHE_DIR}`);
    if (existsSync(CACHE_DIR)) rmSync(CACHE_DIR, { recursive: true, force: true });
    mkdirSync(dirname(CACHE_DIR), { recursive: true });
    run("git", ["clone", "--depth=1", REPO_URL, CACHE_DIR]);
}

function buildBundle() {
    const entries = {};
    let parsed = 0;
    let skipped = 0;
    const skippedReasons = { noBoardName: 0, noMotors: 0 };

    const files = readdirSync(CONFIGS_DIR).filter((name) => name.endsWith(".config"));
    for (const file of files) {
        const text = readFileSync(join(CONFIGS_DIR, file), "utf8");
        const { boardName, manufacturerId, motors, ledStrips } = parseUnifiedTargetConfig(text);
        if (!boardName) {
            skipped += 1;
            skippedReasons.noBoardName += 1;
            continue;
        }
        if (motors.length === 0) {
            skipped += 1;
            skippedReasons.noMotors += 1;
            continue;
        }
        // Modern FC firmware reports `FC.CONFIG.boardName` as
        // `<MFGR>_<BOARDNAME>` (e.g. `TMTR_TMOTORF7`). Use that as the
        // primary key so the lookup helper sees a direct hit on the
        // most-common shape. The lookup helper additionally falls back
        // to bare boardName for older firmware that drops the prefix.
        const key = manufacturerId ? `${manufacturerId}_${boardName}` : boardName;
        if (entries[key]) {
            // Multiple configs for the same board (common: rev variants).
            // Keep the one with more motors; tiebreak by more LEDs. This
            // mirrors how a pilot reads silkscreen — most-complete map
            // wins. Source filename is recorded for debug.
            const prev = entries[key];
            const isBetter =
                motors.length > prev.motors.length ||
                (motors.length === prev.motors.length && ledStrips.length > prev.ledStrips.length);
            if (!isBetter) continue;
        }
        entries[key] = {
            schemaVersion: 1,
            boardName,
            manufacturerId,
            sourceFile: file,
            motors,
            ledStrips,
        };
        parsed += 1;
    }

    console.log(`[sync] parsed ${parsed} targets, skipped ${skipped}`);
    console.log(`[sync]   skipped (no board_name): ${skippedReasons.noBoardName}`);
    console.log(`[sync]   skipped (no motors):    ${skippedReasons.noMotors}`);
    return entries;
}

function writeBundle(entries) {
    mkdirSync(dirname(OUTPUT_FILE), { recursive: true });
    const sorted = {};
    for (const key of Object.keys(entries).sort()) {
        sorted[key] = entries[key];
    }
    const payload = {
        $schema:
            "https://github.com/betaflight/betaflight-configurator (target silkscreen-defaults bundle, generated by scripts/sync-unified-targets.mjs)",
        generatedAt: new Date().toISOString(),
        sourceRepo: REPO_URL,
        targets: sorted,
    };
    writeFileSync(OUTPUT_FILE, JSON.stringify(payload, null, 4) + "\n", "utf8");
    console.log(`[sync] wrote ${OUTPUT_FILE} with ${Object.keys(sorted).length} targets`);
}

function main() {
    ensureClone();
    const entries = buildBundle();
    writeBundle(entries);
}

main();
